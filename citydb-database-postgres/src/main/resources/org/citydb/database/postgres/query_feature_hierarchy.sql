WITH RECURSIVE FEATURE_HIERARCHY AS
  (SELECT NULL::bigint AS ID,
     ID AS FEATURE_ID,
     NULL::bigint AS PARENT_ID,
     NULL::int AS DATATYPE_ID,
     NULL::int AS NAMESPACE_ID,
     NULL AS NAME,
     NULL::bigint AS VAL_INT,
     NULL::double precision AS VAL_DOUBLE,
     NULL AS VAL_STRING,
     NULL::TIMESTAMP WITH TIME ZONE AS VAL_TIMESTAMP,
     NULL AS VAL_URI,
     NULL AS VAL_CODESPACE,
     NULL AS VAL_UOM,
     NULL::json AS VAL_ARRAY,
     NULL AS VAL_LOD,
     NULL::bigint AS VAL_GEOMETRY_ID,
     NULL::bigint AS VAL_IMPLICITGEOM_ID,
     NULL::GEOMETRY AS VAL_IMPLICITGEOM_REFPOINT,
     NULL::bigint AS VAL_APPEARANCE_ID,
     NULL::bigint AS VAL_ADDRESS_ID,
     ID AS VAL_FEATURE_ID,
     1 VAL_REFERENCE_TYPE,
     NULL AS VAL_CONTENT,
     NULL AS VAL_CONTENT_MIME_TYPE,
     FALSE AS IS_CYCLE,
     ARRAY[]::bigint[] AS PATH
   FROM %SCHEMA%.FEATURE
   WHERE ID = ?
   UNION ALL SELECT
     P.ID,
     P.FEATURE_ID,
     P.PARENT_ID,
     P.DATATYPE_ID,
     P.NAMESPACE_ID,
     P.NAME,
     P.VAL_INT,
     P.VAL_DOUBLE,
     P.VAL_STRING,
     P.VAL_TIMESTAMP,
     P.VAL_URI,
     P.VAL_CODESPACE,
     P.VAL_UOM,
     P.VAL_ARRAY,
     P.VAL_LOD,
     P.VAL_GEOMETRY_ID,
     P.VAL_IMPLICITGEOM_ID,
     P.VAL_IMPLICITGEOM_REFPOINT,
     P.VAL_APPEARANCE_ID,
     P.VAL_ADDRESS_ID,
     P.VAL_FEATURE_ID,
     P.VAL_REFERENCE_TYPE,
     P.VAL_CONTENT,
     P.VAL_CONTENT_MIME_TYPE,
     P.ID = ANY(PATH),
     PATH || P.ID
   FROM %SCHEMA%.PROPERTY P
   INNER JOIN FEATURE_HIERARCHY H ON H.VAL_FEATURE_ID = P.FEATURE_ID AND H.VAL_REFERENCE_TYPE = 1 AND NOT IS_CYCLE)
SELECT
  H.ID,
  H.FEATURE_ID,
  H.PARENT_ID,
  H.DATATYPE_ID,
  H.NAMESPACE_ID,
  H.NAME,
  H.VAL_INT,
  H.VAL_DOUBLE,
  H.VAL_STRING,
  H.VAL_TIMESTAMP,
  H.VAL_URI,
  H.VAL_CODESPACE,
  H.VAL_UOM,
  H.VAL_ARRAY,
  H.VAL_LOD,
  H.VAL_GEOMETRY_ID,
  H.VAL_IMPLICITGEOM_ID,
  H.VAL_IMPLICITGEOM_REFPOINT,
  H.VAL_APPEARANCE_ID,
  H.VAL_ADDRESS_ID,
  H.VAL_FEATURE_ID,
  H.VAL_REFERENCE_TYPE,
  H.VAL_CONTENT,
  H.VAL_CONTENT_MIME_TYPE,
  F.OBJECTCLASS_ID,
  F.OBJECTID,
  F.IDENTIFIER,
  F.IDENTIFIER_CODESPACE,
  F.ENVELOPE,
  F.LAST_MODIFICATION_DATE,
  F.UPDATING_PERSON,
  F.REASON_FOR_UPDATE,
  F.LINEAGE,
  F.CREATION_DATE,
  F.TERMINATION_DATE,
  F.VALID_FROM,
  F.VALID_TO,
  G.GEOMETRY,
  G.GEOMETRY_PROPERTIES,
  G.FEATURE_ID AS GEOMETRY_FEATURE_ID,
  A.OBJECTID AS ADDRESS_OBJECT_ID,
  A.IDENTIFIER AS ADDRESS_IDENTIFIER,
  A.IDENTIFIER_CODESPACE AS ADDRESS_IDENTIFIER_CODESPACE,
  A.STREET,
  A.HOUSE_NUMBER,
  A.PO_BOX,
  A.ZIP_CODE,
  A.CITY,
  A.STATE,
  A.COUNTRY,
  A.FREE_TEXT,
  A.MULTI_POINT,
  A.CONTENT,
  A.CONTENT_MIME_TYPE
FROM FEATURE_HIERARCHY H
LEFT JOIN %SCHEMA%.FEATURE F ON F.ID = H.VAL_FEATURE_ID
LEFT JOIN %SCHEMA%.GEOMETRY_DATA G ON G.ID = H.VAL_GEOMETRY_ID
LEFT JOIN %SCHEMA%.ADDRESS A ON A.ID = H.VAL_ADDRESS_ID
WHERE NOT IS_CYCLE